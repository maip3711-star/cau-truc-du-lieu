# =====================================
# 1. Insertion Sort
# =====================================
def insertion_sort(a):
    for i in range(1, len(a)):
        key = a[i]
        j = i - 1

        while j >= 0 and a[j] > key:
            a[j + 1] = a[j]
            j -= 1

        a[j + 1] = key


# =====================================
# 2. Quick Sort (median-of-three)
# =====================================
def median_of_three(a, left, right):
    mid = (left + right) // 2

    if a[left] > a[mid]:
        a[left], a[mid] = a[mid], a[left]
    if a[left] > a[right]:
        a[left], a[right] = a[right], a[left]
    if a[mid] > a[right]:
        a[mid], a[right] = a[right], a[mid]

    # đưa pivot về vị trí right-1
    a[mid], a[right - 1] = a[right - 1], a[mid]
    return a[right - 1]


def quick_sort(a, left, right):
    if left >= right:
        return

    # tối ưu đoạn nhỏ (<=10 phần tử)
    if right - left + 1 <= 10:
        for i in range(left + 1, right + 1):
            key = a[i]
            j = i - 1
            while j >= left and a[j] > key:
                a[j + 1] = a[j]
                j -= 1
            a[j + 1] = key
        return

    pivot = median_of_three(a, left, right)

    i = left
    j = right - 1

    while True:
        i += 1
        while a[i] < pivot:
            i += 1

        j -= 1
        while a[j] > pivot:
            j -= 1

        if i < j:
            a[i], a[j] = a[j], a[i]
        else:
            break

    # đưa pivot về đúng vị trí
    a[i], a[right - 1] = a[right - 1], a[i]

    quick_sort(a, left, i - 1)
    quick_sort(a, i + 1, right)


