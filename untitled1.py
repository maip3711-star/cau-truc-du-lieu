# 3. Merge Sort
# =====================================
def merge(a, left, mid, right):
    temp = []
    i = left
    j = mid + 1

    while i <= mid and j <= right:
        if a[i] <= a[j]:
            temp.append(a[i])
            i += 1
        else:
            temp.append(a[j])
            j += 1

    while i <= mid:
        temp.append(a[i])
        i += 1

    while j <= right:
        temp.append(a[j])
        j += 1

    for k in range(len(temp)):
        a[left + k] = temp[k]


def merge_sort(a, left, right):
    if left >= right:
        return

    mid = (left + right) // 2
    merge_sort(a, left, mid)
    merge_sort(a, mid + 1, right)
    merge(a, left, mid, right)


